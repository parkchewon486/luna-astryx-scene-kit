function renderCharacterSheetPreset() {
  const list = getPresetList();
  if (!list) return;

  characterSheetMode = true;
  setCharacterTabActive();
  list.innerHTML = characterCardMarkup();

  const card = list.querySelector<HTMLButtonElement>('[data-character-sheet-card="true"]');
  card?.addEventListener('click', (event) => {
    if ((event.target as HTMLElement | null)?.closest('.forceRealPreview')) return;
    applyCharacterSheetPreset();
  });

  applyCharacterSheetPreset();
}

function installCharacterCopyHook() {
  if (document.body.dataset.characterSheetCopyHook === 'true') return;
  document.body.dataset.characterSheetCopyHook = 'true';

  document.addEventListener('click', (event) => {
    if (!characterSheetMode) return;
    const button = (event.target as HTMLElement | null)?.closest('button');
    if (!button?.textContent?.includes('전체 프롬프트 복사')) return;
    window.setTimeout(() => {
      navigator.clipboard?.writeText(characterFullPrompt()).catch(() => undefined);
    }, 70);
  }, true);
}

function runCharacterSheetPreset() {
  installCharacterSheetStyles();
  installCharacterCopyHook();
  ensureCharacterSheetTab();

  const activeCustomTab = document.querySelector<HTMLElement>('[data-character-sheet-tab="true"]');
  if (characterSheetMode || activeCustomTab?.classList.contains('active')) {
    renderCharacterSheetPreset();
  }
}

runCharacterSheetPreset();

const characterSheetObserver = new MutationObserver(() => {
  window.clearTimeout(characterSheetTimer);
  characterSheetTimer = window.setTimeout(runCharacterSheetPreset, 120);
});

characterSheetObserver.observe(document.documentElement, { childList: true, subtree: true });
